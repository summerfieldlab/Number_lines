In the following experiment, you will see a stream of numbers, some numbers
will be coloured (targets) and the rest will be plain white (fillers). You
will perform two tasks at the same time.

Task A: 
Judge whether a coloured target number is HIGHER or LOWER 
than the previous target number.

Current target > previous target    -> UP ARROW     (e.g 34 > 31)
Current target < previous target    -> DOWN ARROW   (e.g. 25 < 28)

Task B: 
The targets will be mixed with white filler numbers. Pay attention to
these numbers, because you will also have to judge whether a filler has 
the SAME value as the last target number.

Filler = target     -> SPACE BAR    (e.g. target 25 = filler 25)